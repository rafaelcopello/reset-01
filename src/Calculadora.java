public class Calculadora {

    int somar(int valor1,int valor2) {
        return valor1 + valor2;
    }
    int subtrai(int valor1,int valor2) {
        return valor1 - valor2;
    }

    int multiplica(int valor1,int valor2) {
        return valor1 * valor2;
    }

    int divide(int valor1,int valor2) {
        return valor1 / valor2;
    }
}

